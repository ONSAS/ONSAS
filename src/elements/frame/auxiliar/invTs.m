function [De]=invTs(t);

nt=norm(t);
I=eye(3,3);

if nt==0
  De=I;
else
  b=nt/2;
  a=(sin(b)-b*cos(b))/(nt^2*sin(b));
  M=skew(t);
  De=I-1/2*M+a*M*M;
end
